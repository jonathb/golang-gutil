package gutil

import "testing"

func TestReverse(t *testing.T) {
	cases := []struct {
		in, want string
	}{
		{"Hello, world", "dlrow ,olleH"},
		{"dlrow ,olleH","Hello, world"},
		{"", ""},
// UNCOMMENT TO CONTRIVE A FAIL              {"bogus", ""},
		{"123", "321"},
	}
	for _, c := range cases {
		got := Reverse(c.in)
		if got != c.want {
			t.Errorf("Reverse(%q) == %q, want %q", c.in, got, c.want)
		}
	}
}
